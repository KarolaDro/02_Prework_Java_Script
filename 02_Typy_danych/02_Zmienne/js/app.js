// Ta zmienna jest typu number i przechowuje wartość 25
const numberValue = 25;
console.log(numberValue);
console.log(typeof numberValue);

// Ta zmienna jest typu string i przechowuje tekst
const stringValue = "To jest mój string";
console.log(stringValue);
console.log(typeof stringValue);

// Ta zmienna jest typu string i łączy wartość i tekst
const number = 2;
const text = "dwa";
const mixedValue = number + " " + text;
console.log(mixedValue);
console.log(typeof mixedValue);

// Ta zmienna jest typu boolean i przechowuje wartość logiczną
const logicValue = true;
console.log(logicValue);
console.log(typeof logicValue);

// Ta zmienna jest typu object i przechowuje pustą wartość
const nullValue = null;
console.log(nullValue);
console.log(typeof nullValue);